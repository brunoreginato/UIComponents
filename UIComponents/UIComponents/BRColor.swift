import Foundation
import UIKit

extension UIColor {
    static func BRRed() -> UIColor {
        return UIColor(red: 222.0/255.0, green:45.0/255.0, blue:45.0/255.0, alpha:1.0)
    }
    
    static func BRGray() -> UIColor {
        return UIColor(red: 120.0/255.0, green:120.0/255.0, blue:120.0/255.0, alpha:1.0)
    }
}